#As we did in the session 1, we can use variables to store strings
str_1 = "Hello"
str_2 = "World"
print(str_1)
print(str_2)
#What happen if we want to show the same text but together? We use concat operator (+)
print(str_1 + " " + str_2)
#Also we can make variables of two strings like this
str_3 = "Hello" " World"
print(str_3)
#Something a little bit strage is multiply strings
str_4 = " Hello" * 10
print(str_4)